CREATE TABLE USER_ACCOUNTS (
    USER_ID   NUMBER PRIMARY KEY,
    USER_NAME VARCHAR2(50),
    USER_PASSWORD VARCHAR2(50),
    ROLE_NAME VARCHAR2(30) REFERENCES USER_ROLES(ROLE_NAME)
);

